$(document).ready(
function() {

    $('.slideshow_image img').fadeOut(1500, function(){
        $(this).attr('src', './images/slideshow/slideshow_2.jpg')
    }).fadeIn(1500,function(){
        $(this).fadeOut(1500, function(){
            $(this).attr('src', './images/slideshow/slideshow_3.jpg')
        }).fadeIn(1500,function(){
            $(this).fadeOut(1500,function(){
                $(this).attr('src', './images/slideshow/slideshow_1.jpg')
            }).fadeIn(1500);
        });
    });


    $('.switcher a').click(function(eventObject) {
        $('.slideshow_image img').attr('src',$(this).attr('href'));
        $('.slideshow_image img').load(function(){
           $(this).show();
        });
        eventObject.preventDefault();
    });

});


